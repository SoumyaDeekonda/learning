namespace calculator
{
    //test
    public class Operation
    {

        public int Add(int a, int b)
        {
            return a + b;
        }

        public int Sub(int input1, int input2)
        {
            return input1 - input2;
        }

        public double Div(double i, double i1)
        {
            return i/i1;
        }

        public double Mul(int i, int i1)
        {
            return i * i1;
        }
    }
}
